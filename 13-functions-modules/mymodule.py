#nó chỉ định nghĩa các phép toán sum,subtract,multiply,divide nhưung không chạy được phép toán đó với con số cụ thể nào

def sum(a,b):
    c = a + b
    return c
def subtract(a,b):
    c = a - b
    return c

def multiply(a,b):
    c = a * b
    return c

def divide(a,b):
    c = a / b
    return c 